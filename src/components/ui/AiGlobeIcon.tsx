interface AiGlobeIconProps {
  active: boolean;
  size?: number;
  /** משך סיבוב מלא אחד, בשניות. */
  durationSeconds?: number;
}

/**
 * גלובוס נקודות תלת-ממדי אמיתי, מצויר בקנבס.
 * כל נקודה ממוקמת לפי קו-אורך/קו-רוחב אמיתי ומוטלת על פני כדור מסתובב —
 * לא הזזה של תמונה שטוחה. הנקודות תמיד באותו גודל וחדות, רק המיקום שלהן זז.
 */
export function AiGlobeIcon({ active, size = 56, durationSeconds = 22 }: AiGlobeIconProps) {
  const canvasRef = useRef<HTMLCanvasElement | null>(null);
  const rafRef = useRef<number | null>(null);
  const angleRef = useRef(0);
  const lastTsRef = useRef<number | null>(null);

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    const dpr = typeof window !== "undefined" ? window.devicePixelRatio || 1 : 1;
    canvas.width = size * dpr;
    canvas.height = size * dpr;
    canvas.style.width = `${size}px`;
    canvas.style.height = `${size}px`;
    ctx.scale(dpr, dpr);

const cx = size / 2;
    const cy = size / 2;
    const sphereRadius = size * 0.46;
    const dotRadius = Math.max(1.4, size * 0.032);
    const color = active ? "#1877F2" : "#0f1522";
    function draw(angle: number) {
      if (!ctx) return;
      ctx.clearRect(0, 0, size, size);
      ctx.fillStyle = color;

      for (const [lonDeg, latDeg] of GLOBE_POINTS) {
        const lon = (lonDeg * Math.PI) / 180 + angle;
        const lat = (latDeg * Math.PI) / 180;

        // הטלת נקודת קו-אורך/קו-רוחב על כדור יחידה, אחרי סיבוב סביב ציר Y
        const x = Math.cos(lat) * Math.sin(lon);
        const y = Math.sin(lat);
        const z = Math.cos(lat) * Math.cos(lon);

        if (z <= 0) continue; // צד אחורי של הכדור — לא מציירים

        const screenX = cx + x * sphereRadius;
        const screenY = cy - y * sphereRadius;

        // דהייה עדינה קרוב לקצה הכדור (z→0), בלי לשנות את גודל הנקודה עצמה
        const opacity = Math.min(1, z * 1.6);

        ctx.globalAlpha = opacity;
        ctx.beginPath();
        ctx.arc(screenX, screenY, dotRadius, 0, Math.PI * 2);
        ctx.fill();
      }
      ctx.globalAlpha = 1;
    }

    const anglePerMs = (Math.PI * 2) / (durationSeconds * 1000);

    function tick(ts: number) {
      if (lastTsRef.current == null) lastTsRef.current = ts;
      const delta = ts - lastTsRef.current;
      lastTsRef.current = ts;
      angleRef.current += anglePerMs * delta;
      draw(angleRef.current);
      rafRef.current = requestAnimationFrame(tick);
    }

    rafRef.current = requestAnimationFrame(tick);

    return () => {
      if (rafRef.current != null) cancelAnimationFrame(rafRef.current);
      lastTsRef.current = null;
    };
  }, [active, size, durationSeconds]);

  return <canvas ref={canvasRef} aria-hidden="true" />;
}